def trans(wlens):
    """ Give transmission through a TLMB optic as a function of wavelengths. Range is only valid from 500 to 1060, so does simple interpolation to last value outside this.
    Inputs:
        wlens: Numpy array or list of wavelengths, in nm, in ASCENDING order
    Outputs:
        trans: Transmission fraction (in range 0 to 1) at input wavelengths        
    """
    trans = np.interp(wlens, wlens0, trans0, right=trans0[0])
    return trans
